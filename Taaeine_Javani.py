Sen=int(input('Lotfan SEN e khod ra vared konid: '))
if (Sen>0) and (Sen<6):
    print('khordsal')
elif (Sen>=6) and (Sen<10):
    print('koodak')
elif (Sen>=10) and (Sen<14):
    print('Nojavan')
elif (Sen>=14) and (Sen<24):
    print('Javan')
elif (Sen>=24) and (Sen<40):
    print('Bozorgsal')
else:
    print('Miansal')
